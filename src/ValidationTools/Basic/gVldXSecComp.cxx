void CompareModels(void)
{
  bool curr_ok = CheckRootFilename(gXSecFilename_curr.c_str());
  bool ref0_ok = CheckRootFilename(gXSecFilename_ref0.c_str());

  if(!curr_ok) {
    return;
  }
  gXSecFile_curr = new TFile(gXSecFilename_curr.c_str(), "read");

  if(ref0_ok) {
    gXSecFile_ref0 = new TFile(gXSecFilename_ref0.c_str(), "read");
  }

  GeneratePlots_1dir("nu_mu_Fe56");

  CompareWithData();
}
